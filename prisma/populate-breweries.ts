// Helper to calculate tier from IQ score
function getTier(iqScore: number): 'DIAMOND' | 'PLATINUM' | 'GOLD' | 'SILVER' | 'BRONZE' {
  if (iqScore >= 95) return 'DIAMOND';
  if (iqScore >= 90) return 'PLATINUM';
  if (iqScore >= 85) return 'GOLD';
  if (iqScore >= 75) return 'SILVER';
  return 'BRONZE';
}

