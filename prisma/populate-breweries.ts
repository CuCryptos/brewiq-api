async function main() {
  console.log('Populating breweries and beers...\n');

  // Phase 1: Upsert all breweries
  let breweryCount = 0;
  const breweryIdMap: Record<string, string> = {};

  for (const brewery of breweries) {
    const result = await prisma.brewery.upsert({
      where: { slug: brewery.slug },
      create: brewery,
      update: {},
    });
    breweryIdMap[brewery.slug] = result.id;
    breweryCount++;
    console.log(`  [${breweryCount}/${breweries.length}] Brewery: ${brewery.name} (${brewery.city}, ${brewery.state})`);
  }

  console.log(`\nCreated/updated ${breweryCount} breweries.\n`);

  // Phase 2: Upsert all beers
  let beerCount = 0;
  let beerSkipped = 0;

  for (const beer of beers) {
    const breweryId = breweryIdMap[beer.brewerySlug];
    if (!breweryId) {
      console.log(`  SKIP: Brewery not found for slug: ${beer.brewerySlug}`);
      beerSkipped++;
      continue;
    }

    const tier = getTier(beer.iqScore);

    await prisma.beer.upsert({
      where: { slug: beer.slug },
      create: {
        name: beer.name,
        slug: beer.slug,
        breweryId,
        style: beer.style,
        description: beer.description,
        abv: beer.abv,
        ibu: beer.ibu,
        iqScore: beer.iqScore,
        tier,
        flavorTags: beer.flavorTags,
        foodPairings: beer.foodPairings,
        availability: beer.availability,
      },
      update: {},
    });
    beerCount++;
    console.log(`  [${beerCount}/${beers.length}] Beer: ${beer.name} (${beer.style}, ${beer.abv}% ABV, IQ: ${beer.iqScore})`);
  }

  // Summary
  console.log('\n========================================');
  console.log('  BREWERY & BEER POPULATION COMPLETE');
  console.log('========================================\n');
  console.log(`Total breweries created/updated: ${breweryCount}`);
  console.log(`Total beers created/updated: ${beerCount}`);
  if (beerSkipped > 0) {
    console.log(`Beers skipped (missing brewery): ${beerSkipped}`);
  }

  // Breakdown by style
  const styleCounts: Record<string, number> = {};
  for (const beer of beers) {
    styleCounts[beer.style] = (styleCounts[beer.style] || 0) + 1;
  }
  console.log('\n--- Beers by Style ---');
  Object.entries(styleCounts)
    .sort((a, b) => b[1] - a[1])
    .forEach(([style, count]) => {
      console.log(`  ${style.padEnd(30)} ${count}`);
    });

  // Breakdown by state
  const stateCounts: Record<string, number> = {};
  for (const brewery of breweries) {
    stateCounts[brewery.state] = (stateCounts[brewery.state] || 0) + 1;
  }
  console.log('\n--- Breweries by State ---');
  Object.entries(stateCounts)
    .sort((a, b) => b[1] - a[1])
    .forEach(([state, count]) => {
      console.log(`  ${state.padEnd(5)} ${count}`);
    });
}

main()
  .catch((e) => {
    console.error('FATAL:', e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
